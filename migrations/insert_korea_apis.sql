## 카테고리별 핵심 API 추천
- **지도 & 위치**: 국내 특화 데이터와 정교한 POI 검색이 중요하다면 **카카오맵** 또는 **네이버 지도**를 추천합니다.
- **금융 & 결제**: 개별 PG사와 복잡하게 연동하기보다 **포트원(PortOne)**을 통하면 토스, 카카오페이 등 다양한 결제 수단을 한 번에 관리할 수 있습니다.
- **공공 서비스**: 실생활 유용한 정보(날씨, 대기질)가 필요하다면 **기상청** 또는 **에어코리아** API를 통해 실시간 데이터를 수집할 수 있습니다.

## 활용 가이드
- **인증 방식**: 대부분 `apiKey` 방식을 사용하며, 사용자 정보가 포함된 서비스는 `OAuth`를 지원하므로 보안 요구 사항을 먼저 확인해야 합니다.
- **트래픽 제한**: 무료 API라도 일일 호출 한도(Quota)가 정해져 있는 경우가 많습니다. 서비스 런칭 전 예상 트래픽을 반드시 계산하세요.
