## 1. 공유기 DDNS 기능 사용
가장 널리 쓰이는 대중적인 방법으로, 가정용 공유기(ipTIME, ASUS, TP-Link 등) 제조사에서 자체적으로 제공하는 무료 DDNS 서버를 활용하는 방식입니다.

공유기 관리자 페이지(예: 192.168.0.1)에 접속하여 DDNS 설정을 활성화하면, 공유기가 주기적으로 자신의 공인 IP 변경을 감지하고 제조사 도메인(예: `id.iptime.org`)으로 연결을 자동 동기화합니다.

- **설정 방법**: 공유기 관리 화면 -> 고급 설정 -> 특수기능 -> DDNS 설정에서 호스트 이름과 사용자 ID(이메일)를 입력하여 등록합니다.
- **포트 포워딩 필수**: 외부에서 개인 서버로 접속하기 위해서는 공유기 설정에서 포트 포워딩(Port Forwarding) 또는 DMZ 설정을 추가로 진행해야 합니다.

## 2. 서버에 DDNS 스크립트 설치
공유기 제조사 도메인 대신 자신이 직접 구매한 개인 커스텀 도메인(예: `myserver.com`)을 Cloudflare 등의 DNS API와 직접 연계하여 연동하고 싶을 때 사용하는 방법입니다.

개인 서버(Linux, Windows, Docker 등) 내부에 크론탭(Crontab)이나 백그라운드 스케줄러로 작동하는 경량 DDNS 업데이트 스크립트를 상주시키고, 서버가 정기적으로 현재 공인 IP를 조회하여 DNS API를 통해 레코드를 자동 갱신하도록 처리합니다.

- **스크립트 예시**: Cloudflare API를 활용한 Linux Bash 쉘 스크립트나 DDNS Docker 이미지(예: `oznu/cloudflare-ddns`)를 가동합니다.
- **작동 방식**: 5~10분 주기로 외부 IP 확인 API를 호출하여, IP가 변경되었을 때만 Cloudflare DNS 레코드를 API 요청으로 수정합니다.

## 3. Cloudflare Tunnel 사용
가장 최신의 상태적이고 보안이 뛰어난 솔루션으로, 외부로 포트를 개방(포트 포워딩)하지 않고도 Cloudflare Edge 네트워크와 개인 서버 간의 암호화된 가상 터널을 통해 안전하게 도메인을 노출시키는 방식입니다.

개인 서버에 `cloudflared` 데몬을 설치하고 로그인하면, 서버가 먼저 Cloudflare로 아웃바운드 터널 연결을 엽니다. 외부 유저는 Cloudflare Edge를 거쳐 서버로 프록시 접속하기 때문에, 개인 서버의 실제 공인 IP가 외부에 전혀 노출되지 않으며 포트 포워딩 조차 불필요합니다.

- **작동 원리**: 서버 -> Cloudflare (Outbound 통신). 포트를 외부로 열 필요가 없으므로 방화벽 설정이 지극히 안전해집니다.
- **부가 기능**: Cloudflare Access와 연동하여 특정 이메일 인증이나 다중 인증(MFA)을 추가로 덧씌워 철저한 보안 장치를 통제할 수 있습니다.

