1. **[클라이언트 식별자 탐색]**: Next.js Edge Middleware 스코프 내에서 요청을 보낸 사용자의 고유 식별자(프록시 환경에서도 신뢰할 수 있는 IP 주소 또는 로그인 Bearer JWT 토큰)를 추출합니다.
